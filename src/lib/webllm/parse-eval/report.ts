/**
 * Report renderers for the parse-resume eval harness (issue #241).
 *
 * Two flavors:
 *   - `renderJsonReport` — structured JSON for machine diffing.
 *   - `renderMarkdownReport` — human-readable table for pasting into
 *     the PR #241 description.
 *
 * Both are pure over `ParseEvalReport` — no side effects.
 *
 * Mirrors the pattern in `src/lib/webllm/eval/report.ts`.
 */

import type { ParseEvalReport } from "./score.ts";

export function renderJsonReport(report: ParseEvalReport): string {
  return `${JSON.stringify(report, null, 2)}\n`;
}

function pct(v: number): string {
  return `${Math.round(v * 100)}%`;
}

function tick(v: boolean): string {
  return v ? "PASS" : "fail";
}

export function renderMarkdownReport(report: ParseEvalReport): string {
  const lines: string[] = [];

  lines.push("# Parse-resume eval report (issue #241)");
  lines.push("");
  lines.push(`- **Started:** ${report.startedAt}`);
  lines.push(`- **Model:** \`${report.modelId}\``);
  lines.push(`- **Fixtures:** ${report.fixtures.length}`);
  lines.push("");

  lines.push("## Overall");
  lines.push("");
  lines.push("| Metric | Value |");
  lines.push("| --- | --- |");
  lines.push(`| Valid-JSON rate | ${pct(report.validJsonRate)} |`);
  lines.push(`| Scalar accuracy (mean) | ${pct(report.meanScalarAccuracy)} |`);
  lines.push(`| Skills accuracy (mean, Jaccard) | ${pct(report.meanSkillsAccuracy)} |`);
  lines.push(`| Experience accuracy (mean) | ${pct(report.meanExperienceAccuracy)} |`);
  lines.push(`| Education accuracy (mean) | ${pct(report.meanEducationAccuracy)} |`);
  lines.push("");

  lines.push("## Per-fixture breakdown");
  lines.push("");
  lines.push(
    "| Fixture | valid-JSON | scalar-acc | skills-acc | exp-acc | edu-acc |",
  );
  lines.push(
    "| --- | --- | --- | --- | --- | --- |",
  );
  for (const f of report.fixtures) {
    lines.push(
      `| ${f.fixtureLabel} | ${tick(f.validJson)} | ${pct(f.scalarAccuracy)} | ${pct(f.skillsAccuracy)} | ${pct(f.experienceAccuracy)} | ${pct(f.educationAccuracy)} |`,
    );
  }
  lines.push("");

  // Scalar field detail — only the fields that missed, so a sub-100% scalar
  // score names exactly which field(s) the model got wrong instead of a bare
  // percentage. Values are synthetic (PII-safe fixtures), so showing
  // expected-vs-actual here cannot leak real PII.
  const missRows: string[] = [];
  for (const f of report.fixtures) {
    for (const s of f.scalarBreakdown) {
      if (s.status === "match" || s.status === "skipped") continue;
      missRows.push(
        `| ${f.fixtureLabel} | ${s.field} | ${s.status} | ${s.expected ?? "—"} | ${s.actual ?? "—"} |`,
      );
    }
  }
  lines.push("## Scalar field misses");
  lines.push("");
  if (missRows.length === 0) {
    lines.push("_None — every expected scalar matched._");
  } else {
    lines.push("| Fixture | Field | Status | Expected | Actual |");
    lines.push("| --- | --- | --- | --- | --- |");
    lines.push(...missRows);
  }
  lines.push("");

  lines.push("---");
  lines.push("");
  lines.push(
    "_Generated by the parse-eval harness. Paste into PR #241 description._",
  );
  lines.push("");

  return lines.join("\n");
}
